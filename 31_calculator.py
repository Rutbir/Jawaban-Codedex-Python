#Membuat program kalkulator sederhana dengan fungsi tambah, kurang, kali, bagi dan pangkat

def add(a, b):
  return a + b
print(add(3, 5))

def subtract(a, b):
  return a - b
print(subtract(5, 3))

def multiply(a, b):
  return a*b
print(multiply(5, 3))

def divide(a, b):
  return a/b
print(divide(6, 3))

def exp(a, b):
  return a**b
  
print(exp(5, 3))